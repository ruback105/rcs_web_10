const country = "Lithuania";

switch (country) {
  case "Lithuania": // country === "Lithuania"
    console.log("Lithuania");
    break;
  case "Estonia": // country === "Estonia"
    console.log("Estonia");
    break;
  case "Germany": // country === "Germany"
    console.log("Germany");
    break;
  case "Latvia": // country === "Latvia"
    console.log("Latvia");
    break;
  default:
    console.log("Unknown country");
}
